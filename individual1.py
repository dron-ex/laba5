
# Ввод списка из 10 элементов
A = []
for i in range(10):
    element = int(input(f"Введите элемент {i + 1}: "))
    A.append(element)

# Нахождение наименьшего элемента и его индекса
min_index = A.index(min(A))

# Меняем местами наименьший элемент с последним элементом
A[min_index], A[-1] = A[-1], A[min_index]

# Вывод преобразованного массива
print("Преобразованный массив:", A)

